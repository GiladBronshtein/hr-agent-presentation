export function getSceneById(id: string): SceneConfig | undefined {
  return SCENES.find((s) => s.id === id);
}

export function getScenesByChapter(chapter: Chapter): SceneConfig[] {
  return SCENES.filter((s) => s.chapter === chapter);
}

export function getCurrentChapter(index: number): Chapter {
  for (const ch of CHAPTERS) {
    if (index >= ch.sceneRange[0] && index <= ch.sceneRange[1]) return ch.id;
  }
  return 'להבין';
}
